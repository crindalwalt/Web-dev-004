console.log("Hello from the Switch page");

/**
 * Switch case is used to check for a specific value
 */

// let age = prompt("Enter your age", "e.g 34");
let age = 40;

switch (age) {
  case 12:
    console.log("Age is 12");
    break;
  case 20:
    console.log("Age is 20");
    break;
  case 40:
    console.log("Age is 40");
    break;
  default:
    console.log("invalid choice");
    break;
}
